// for_each loop is used for only transversal the array
// can't modify the element in the array
package Arrays;

public class for_eachloop {
    static void main(String[] args)
    {
        int [] arr ={5,8,2,6,9};



        for(int ele:arr)
        {
            System.out.print(ele+" ");
        }

    }
}
